# and
a = True
b = True
ResAND = a and b
print(f"a = {a}")
print(f"b = {b}")
print(f"el resultado de a and b = {ResAND}")

a = False
b = True
ResAND = a and b
print(f"a = {a}")
print(f"b = {b}")
print(f"el resultado de a and b = {ResAND}")
# or


a = True
b = False
ResOR = a or b
print(f"a = {a}")
print(f"b = {b}")
print(f"el resultado de a and b = {ResOR}")


